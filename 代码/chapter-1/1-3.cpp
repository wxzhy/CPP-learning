        #include  <iostream>
        using namespace std;
        int  main ( )
        {
            int  length, width, area ;

            cout << "计算矩形的面积 \n" ;
            cout << "输入矩形的长：" ;
            cin >> length ;
            cout << "输入矩形的宽：" ;
            cin >> width ;
            area = length * width ;
            cout << "矩形的面积为：" << area << "\n" ;
            return 0;
        }
